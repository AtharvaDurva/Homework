package Homework;

public class CheckNumIsPositiveOrNegative {
    public static void main(String[] args) {

        int num = -98;
        if(num<0){
            System.out.println(num + "-----> is Negative number");
        }else if(num>0)
        {
            System.out.println(num + "-----> is Positive number" );
        }else
        {
            System.out.println(num + "----->is Zero");
        }


    }

}
